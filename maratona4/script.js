// 2-Exercício While
let clientes = 0;
let MáximoDeClientes = 5;

while(clientes < MáximoDeClientes){
    clientes++;
    console.log(`${clientes} cliente(s) entrou(aram) sorveteria.`);
}
console.log("A sorveteria está cheia");




